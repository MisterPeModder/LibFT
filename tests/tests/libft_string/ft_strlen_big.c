#include "tests.h"

int					ft_strlen_big(void)
{
	int				i;
	char			buff[1024];

	i = -1;
	while (++i < 1023)
		buff[i] = 42;
	buff[i] = 0;
	return (ft_strlen(buff) == 1023);
}
